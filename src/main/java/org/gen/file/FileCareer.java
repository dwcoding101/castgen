public class FileCareer {
    private String name;
    private String stats;
    private String skills;
    private String talents;
    private String careerEntries;

    public String getCareerExits() {
        return careerExits;
    }

    public void setCareerExits(String careerExits) {
        this.careerExits = careerExits;
    }

    private String careerExits;

    public String getCareerEntries() {
        return careerEntries;
    }

    public void setCareerEntries(String careerEntries) {
        this.careerEntries = careerEntries;
    }

    public String getTrappings() {
        return trappings;
    }

    public void setTrappings(String trappings) {
        this.trappings = trappings;
    }

    private String trappings;

    public String getTalents() {
        return talents;
    }

    public void setTalents(String talents) {
        this.talents = talents;
    }

    public String getSkills() {
        return skills;
    }

    public void setSkills(String skills) {
        this.skills = skills;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStats() {
        return stats;
    }

    public void setStats(String stats) {
        this.stats = stats;
    }
}
